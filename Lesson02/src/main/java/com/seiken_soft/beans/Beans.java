package com.seiken_soft.beans;

public class Beans {

	private String id;		//id格納用変数
	private String name;	//名前格納用変数
	private String email;	//アドレス格納用変数

 	public String getId() {
 		return id;
 	}
 	public void setId(String id) {
 		this.id = id;
 	}
 
 	public String getName() {
 		return name;
 	}
 	public void setName(String name) {
 		this.name = name;
 	}
 
 	public String getEmail() {
 		return email;
 	}
 	public void setEmail(String email) {
 		this.email = email;
 	}
}
